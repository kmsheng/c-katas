#include <stdio.h>

int main(void) {

    printf("10 > 5\t\t%d\n", 10 > 5);
    printf("10 >= 5\t\t%d\n", 10 >= 5);
    printf("10 < 5\t\t%d\n", 10 < 5);
    printf("10 <= 5\t\t%d\n", 10 <= 5);
    printf("10 == 5\t\t%d\n", 10 == 5);
    printf("10 != 5\t\t%d\n", 10 != 5);

    return 0;
}
